const cleanup = (mp) => {
  let obj = {};
  for (let key in mp) {
    let newkey = key.split('_').map(x => String.fromCodePoint(parseInt(x, 16))).join('');
    obj[newkey] = mp[key];
  }
  return obj;
}

let sources = cleanup(rawMap);

let restring = '(' +
               Object.keys(sources)
                     .sort(function (a, b) {
                       if (a.length < b.length) return 1;
                       if (a.length > b.length) return -1;
                       return 0;
                     })
                     .join('|')
             + ')';

const emojire = () => RegExp(restring, 'g');

const matchmap = (f, text) => {
  let re = emojire();
  let sindex = 0;
  let aindex = 0;
  let arr = [];
  let match;
  
  do {
    match = re.exec(text);
    if (!match) {
      if (sindex != text.length) {
        arr[aindex] = text.substring(sindex, text.length);
        sindex = text.length;
      }
    } else {
      if (match.index > sindex) {
        arr[aindex] = text.substring(sindex, match.index);
        sindex = sindex + arr[aindex].length;
        aindex = aindex + 1;
      }
      arr[aindex] = f(match);
      sindex = sindex + match[0].length;
      aindex = aindex + 1;
    }
  } while (sindex < text.length);
  return arr;
};

